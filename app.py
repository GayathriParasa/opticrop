def _build_prediction_pdf(result: dict) -> bytes:
    try:
        from reportlab.lib.pagesizes import A4
        from reportlab.lib import colors
        from reportlab.pdfgen import canvas
    except ImportError as exc:
        raise RuntimeError("PDF export dependency missing. Install reportlab.") from exc

    buffer = io.BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=A4)
    width, height = A4
    left = 42
    y = height - 40

    def ensure_space(needed: int):
        nonlocal y
        if y < needed:
            pdf.showPage()
            y = height - 40

    def draw_header():
        nonlocal y
        pdf.setFillColor(colors.HexColor("#14532d"))
        pdf.rect(0, height - 110, width, 110, fill=1, stroke=0)
        pdf.setFillColor(colors.white)
        pdf.setFont("Helvetica-Bold", 24)
        pdf.drawString(left, height - 64, "OptiCrop")
        pdf.setFont("Helvetica", 12)
        pdf.drawString(left, height - 86, "Crop Recommendations Report")
        pdf.setFont("Helvetica", 9)
        pdf.drawRightString(width - left, height - 86, "Generated by OptiCrop ML Engine")
        y = height - 130

    def section_title(text: str):
        nonlocal y
        ensure_space(95)
        pdf.setFillColor(colors.HexColor("#ecfdf5"))
        pdf.setStrokeColor(colors.HexColor("#86efac"))
        pdf.roundRect(left - 4, y - 17, width - (left * 2) + 8, 22, 6, fill=1, stroke=1)
        pdf.setFillColor(colors.HexColor("#14532d"))
        pdf.setFont("Helvetica-Bold", 12)
        pdf.drawString(left + 4, y - 3, text)
        y -= 26

    def body_line(text: str, size: int = 10, bold: bool = False, gap: int = 14):
        nonlocal y
        ensure_space(80)
        pdf.setFillColor(colors.HexColor("#0f172a"))
        pdf.setFont("Helvetica-Bold" if bold else "Helvetica", size)
        pdf.drawString(left, y, text)
        y -= gap

    inputs = result.get("inputs", {})
    location = result.get("location", {})
    location_label = location.get("label") or ", ".join(
        str(v) for v in [location.get("village"), location.get("city"), location.get("district"), location.get("state")] if v
    )
    draw_header()

    section_title("Primary Recommendation")
    body_line(f"Top Crop: {result.get('crop', 'N/A')}", bold=True)
    body_line(f"Prediction Confidence: {result.get('confidence', 'N/A')}%")
    body_line(f"Recommended Season: {result.get('recommended_season', result.get('season', 'N/A'))}")
    body_line(f"Location: {location_label or 'N/A'}")

    y -= 4
    section_title("Analyzed Soil and Weather Inputs")
    body_line(
        f"N: {inputs.get('N', 'N/A')}   P: {inputs.get('P', 'N/A')}   K: {inputs.get('K', 'N/A')}   pH: {inputs.get('ph', 'N/A')}"
    )
    body_line(
        f"Temperature: {inputs.get('temperature', 'N/A')} C   Humidity: {inputs.get('humidity', 'N/A')}%   Rainfall: {inputs.get('rainfall', 'N/A')} mm"
    )

    y -= 4
    section_title("Top Suitable Crops")
    for idx, crop in enumerate((result.get("suitable_crops") or [])[:8], start=1):
        body_line(
            f"{idx}. {crop.get('crop', 'N/A')} | Overall: {crop.get('combined_score', crop.get('confidence', 'N/A'))}% | "
            f"ML: {crop.get('confidence', 'N/A')}% | Season Fit: {crop.get('season_fit', 'N/A')}%",
            size=9,
            gap=13,
        )

    y -= 4
    section_title("Soil and Climate Analysis")
    for a in result.get("analysis", [])[:10]:
        body_line(f"- {a.get('label', 'Parameter')}: {a.get('status', 'N/A')} ({a.get('pct', 'N/A')}% fit)", size=9)

    y -= 10
    ensure_space(65)
    pdf.setStrokeColor(colors.HexColor("#cbd5e1"))
    pdf.line(left, y, width - left, y)
    y -= 14
    pdf.setFillColor(colors.HexColor("#334155"))
    pdf.setFont("Helvetica", 9)
    pdf.drawString(left, y, f"Model: {result.get('model_name', 'Random Forest')} | Accuracy: {result.get('model_accuracy', 'N/A')}%")
    pdf.drawRightString(width - left, y, "Generated by OptiCrop")

    pdf.save()
    buffer.seek(0)
    return buffer.read()

