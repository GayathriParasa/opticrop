def create_app() -> Flask:
    app = Flask(__name__)
    app.config["SECRET_KEY"] = "opticrop-dev-key-change-in-production"

    @app.context_processor
    def inject_globals():
        metrics = get_metrics() if MODEL_PATH.exists() else {}
        return {
            "model_metrics": metrics,
            "model_ready": MODEL_PATH.exists(),
        }

    @app.route("/")
    def index():
        return render_template("index.html")

    @app.route("/workflow")
    def workflow():
        return render_template("workflow.html")

    @app.route("/prediction")
    def prediction():
        return redirect(url_for("workflow"))

    @app.route("/api/locations/search")
    def api_locations_search():
        query = request.args.get("q", "").strip()
        if len(query) < 2:
            return jsonify([])
        try:
            return jsonify(search_locations(query))
        except Exception:
            return jsonify([])

    @app.route("/api/weather/live", methods=["POST"])
    def api_weather_live():
        data = request.get_json(silent=True) or {}
        lat = data.get("lat")
        lon = data.get("lon")
        if lat is None or lon is None:
            return jsonify({"error": "Location coordinates required."}), 400
        try:
            weather = fetch_live_weather(float(lat), float(lon))
            weather["location"] = data.get("label", "")
            return jsonify(weather)
        except Exception:
            return jsonify({"error": "Could not fetch live weather for this location."}), 502

    @app.route("/api/weather", methods=["POST"])
    def api_weather():
        """Legacy demo endpoint — prefer /api/weather/live."""
        data = request.get_json(silent=True) or {}
        lat, lon = data.get("lat"), data.get("lon")
        if lat is not None and lon is not None:
            try:
                return jsonify(fetch_live_weather(float(lat), float(lon)))
            except Exception:
                pass
        state = (data.get("state") or "Default").lower()
        # Demo weather profiles by region name hash
        profiles = {
            "karnataka": {"temperature": 26.5, "humidity": 78, "rainfall": 210},
            "maharashtra": {"temperature": 28.0, "humidity": 72, "rainfall": 185},
            "punjab": {"temperature": 22.0, "humidity": 65, "rainfall": 120},
            "kerala": {"temperature": 29.0, "humidity": 85, "rainfall": 280},
            "tamil nadu": {"temperature": 30.5, "humidity": 70, "rainfall": 195},
        }
        profile = profiles.get(state, {"temperature": 25.0, "humidity": 80, "rainfall": 200})
        return jsonify({"source": "Weather API (demo)", **profile})

    @app.route("/api/upload-soil", methods=["POST"])
    def api_upload_soil():
        if "file" not in request.files:
            return jsonify({"error": "No file uploaded."}), 400

        upload = request.files["file"]
        if not upload.filename:
            return jsonify({"error": "No file selected."}), 400

        try:
            data = parse_upload(upload.filename, upload.read())
            return jsonify(data)
        except ValueError as exc:
            return jsonify({"error": str(exc)}), 400
        except Exception:
            return jsonify({"error": "Could not read the uploaded file."}), 400

    @app.route("/prediction/form", methods=["GET", "POST"])
    def prediction_form():
        result = None
        error = None
        form_data = {}

        if request.method == "POST":
            form_data = {
                "N": request.form.get("N", ""),
                "P": request.form.get("P", ""),
                "K": request.form.get("K", ""),
                "temperature": request.form.get("temperature", ""),
                "humidity": request.form.get("humidity", ""),
                "ph": request.form.get("ph", ""),
                "rainfall": request.form.get("rainfall", ""),
            }

            if not MODEL_PATH.exists():
                error = "ML model not found. Ensure models/random_forest.pkl is present."
            else:
                cleaned, validation_error = validate_inputs(form_data)
                if validation_error:
                    error = validation_error
                else:
                    result = predict(cleaned)

        return render_template(
            "prediction.html",
            result=result,
            error=error,
            form_data=form_data,
        )

    @app.route("/about")
    def about():
        return render_template("about.html")

    @app.route("/documentation")
    def documentation():
        default_slug = DOC_GROUPS[0]["items"][0][0]
        return redirect(url_for("documentation_detail", doc_slug=default_slug))

    @app.route("/documentation/<doc_slug>")
    def documentation_detail(doc_slug: str):
        if doc_slug not in DOC_CONTENT:
            return redirect(url_for("documentation"))

        current_doc = DOC_CONTENT[doc_slug]
        current_group = next(
            (group for group in DOC_GROUPS if any(slug == doc_slug for slug, _ in group["items"])),
            DOC_GROUPS[0],
        )
        current_sections = _build_doc_sections(doc_slug, current_doc["sections"])
        return render_template(
            "documentation.html",
            doc_groups=DOC_GROUPS,
            current_slug=doc_slug,
            current_doc=current_doc,
            current_group=current_group,
            current_sections=current_sections,
        )

    @app.route("/api/predict", methods=["POST"])
    def api_predict():
        if not MODEL_PATH.exists():
            return jsonify({"error": "ML model not found. Ensure models/random_forest.pkl is present."}), 503

        data = request.get_json(silent=True) or {}
        cleaned, validation_error = validate_inputs(data)
        if validation_error:
            return jsonify({"error": validation_error}), 400

        return jsonify(predict(cleaned))

    @app.route("/api/predict/pdf", methods=["POST"])
    def api_predict_pdf():
        result = request.get_json(silent=True) or {}
        if not result or "crop" not in result:
            return jsonify({"error": "Prediction result payload is required."}), 400

        try:
            pdf_bytes = _build_prediction_pdf(result)
        except RuntimeError as exc:
            return jsonify({"error": str(exc)}), 503

        return send_file(
            io.BytesIO(pdf_bytes),
            mimetype="application/pdf",
            as_attachment=True,
            download_name="Crop_recommendations.pdf",
        )

    @app.route("/api/health")
    def health():
        metrics = get_metrics() if MODEL_PATH.exists() else {}
        return jsonify(
            {
                "status": "ok" if MODEL_PATH.exists() else "model_missing",
                "model_loaded": MODEL_PATH.exists(),
                "accuracy": metrics.get("accuracy"),
                "crops": metrics.get("num_crops"),
            }
        )

    return app


app = create_app()

if __name__ == "__main__":
    app.run(debug=True, host="0.0.0.0", port=5000)
